#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		s1_size;
	int		i;

	s1_size = 0;
	i = 0;
	while (s1[s1_size] != '\0')
		s1_size++;
	while (s2[i] != '\0')
	{
		s1[s1_size] = s2[i];
		s1_size++;
		i++;
	}
	s1[s1_size] = '\0';
	return (s1);
}
