#include "libft.h"

void	*memcat(char *s1, size_t s1_size,
			char *s2, size_t s2_size)
{
	size_t	i;

	i = 0;
	while (i < s2_size)
	{
		s1[s1_size] = s2[i];
		s1_size++;
		i++;
	}
	return (s1);
}
